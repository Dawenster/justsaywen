    <p>Which is safer?</p>

    <p><strong>A</strong>. Wearing a helmet while snowboarding; or,<br>
    <strong> B</strong>. Not wearing a helmet while snowboarding.</p>

    <p>I will argue <em>against </em>the obvious answer of A.  And then I will argue why I personally would still do A :)</p>

    <p>I think when a person feels like he is safe, he will take more risks, and thus result in more injuries.  On the day that I wore a helmet snowboarding, I felt more daring and decided to do more jumps and rails.  I was inspired by all the cool guys ahead of me in army snow pants that just glided along the rails while grabbing their boards.  I ended up gliding along the hard snow while grabbing my hurting elbow (it hit the rail on the way down).</p>

    <p>All the time sitting on my hurting butt also got me thinking: maybe if I didn't have a helmet, I would have been more cautious, and thus been safer overall.  Perhaps without a helmet, the expected pain will be lower than with a helmet.  Thinking led to me proving with arbitrary numbers.</p>

    <p>Let the pain of a fall be 4 with a helmet and 8 without.  Let the pain of no fall be 0.<br>
    Let the probability of falling be 75% with a helmet (due to more risks taken) and 25% without one.</p>

    <p>Helmet:<br>
    E(p) = 0.75 * 4 = 3</p>

    <p>Without Helmet:<br>
    E(p) = 0.25 * 8 = 2</p>

    <p>Helmet Pain of 3 &gt; Without Helmet Pain of 2</p>

    <p>I've started to extrapolate this to other aspects of life (as I tend to do with meaningless thoughts to try and squeeze meaning out of them).  Perhaps sometimes it's better to feel vulnerable, because then you will be forced to act smarter.  But then, if you feel safe, you will take more risks in life, potentially going to greater heights (and lows).  So if falling harder by 1 point more doesn't mean much to you, then I advocate taking that risk and putting on the helmet.  For me, I know that with a helmet I will dare to get better, and that will make my E(happiness) = 10.</p>
